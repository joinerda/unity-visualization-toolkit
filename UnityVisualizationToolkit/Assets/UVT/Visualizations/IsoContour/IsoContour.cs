		public static TriangleList getPolygons(GridCell grid, float isolevel, ColorMap cm) {
			
			TriangleList retval = new TriangleList ();
			int nTriangles = Polygonise (grid, isolevel, cm);

			for (int i = 0; i < nTriangles; i++) {
				retval.addTriangle (grid.triangles [i].p [0], grid.triangles[i].p [1], grid.triangles [i].p [2],
					grid.triangles[i].colors[0],grid.triangles[i].colors[1],grid.triangles[i].colors[2]);
			}
			return retval;

		}

