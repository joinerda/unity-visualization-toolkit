		public static int Polygonise(GridCell grid,float isolevel, ColorMap cm)
		{
			int i,ntriang;
			int cubeindex;


			/*
      Determine the index into the edge table which
      tells us which vertices are inside of the surface
   */
			cubeindex = 0;
			if (grid.val[0] < isolevel) cubeindex |= 1;
			if (grid.val[1] < isolevel) cubeindex |= 2;
			if (grid.val[2] < isolevel) cubeindex |= 4;
			if (grid.val[3] < isolevel) cubeindex |= 8;
			if (grid.val[4] < isolevel) cubeindex |= 16;
			if (grid.val[5] < isolevel) cubeindex |= 32;
			if (grid.val[6] < isolevel) cubeindex |= 64;
			if (grid.val[7] < isolevel) cubeindex |= 128;


			/* Cube is entirely in/out of the surface */
			if (edgeTable[cubeindex] == 0)
				return(0);
			/* Find the vertices where the surface intersects the cube */
			int et = edgeTable [cubeindex];

			if ((et & 1) > 0) {
				grid.vertlist [0] =
					VertexInterp (isolevel, grid.p [0], grid.p [1], grid.val [0], grid.val [1]);
				grid.clist [0] =
					ValInterp (isolevel, grid.cValues [0], grid.cValues [1], grid.val [0], grid.val [1]);			
			}
			if ((et & 2) > 0) {
				grid.vertlist [1] =
					VertexInterp (isolevel, grid.p [1], grid.p [2], grid.val [1], grid.val [2]);
				grid.clist [1] =
					ValInterp (isolevel, grid.cValues [1], grid.cValues [2], grid.val [1], grid.val [2]);
			}
			if ((et & 4) > 0) {
				grid.vertlist [2] =
					VertexInterp (isolevel, grid.p [2], grid.p [3], grid.val [2], grid.val [3]);
				grid.clist [2] =
					ValInterp (isolevel, grid.cValues [2], grid.cValues [3], grid.val [2], grid.val [3]);
			}
			if ((et & 8) > 0) {
				grid.vertlist [3] =
					VertexInterp (isolevel, grid.p [3], grid.p [0], grid.val [3], grid.val [0]);
				grid.clist [3] =
					ValInterp (isolevel, grid.cValues [3], grid.cValues [0], grid.val [3], grid.val [0]);
			}
			if ((et & 16) > 0) {
				grid.vertlist [4] =
					VertexInterp (isolevel, grid.p [4], grid.p [5], grid.val [4], grid.val [5]);
				grid.clist [4] =
					ValInterp (isolevel, grid.cValues [4], grid.cValues [5], grid.val [4], grid.val [5]);
			}
			if ((et & 32) > 0) {
				grid.vertlist [5] =
					VertexInterp (isolevel, grid.p [5], grid.p [6], grid.val [5], grid.val [6]);
				grid.clist [5] =
					ValInterp (isolevel, grid.cValues [5], grid.cValues [6], grid.val [5], grid.val [6]);
			}
			if ((et & 64) > 0) {
				grid.vertlist [6] =
					VertexInterp (isolevel, grid.p [6], grid.p [7], grid.val [6], grid.val [7]);
				grid.clist [6] =
					ValInterp (isolevel, grid.cValues [6], grid.cValues [7], grid.val [6], grid.val [7]);
			}
			if ((et & 128) > 0) {
				grid.vertlist [7] =
					VertexInterp (isolevel, grid.p [7], grid.p [4], grid.val [7], grid.val [4]);
				grid.clist [7] =
					ValInterp (isolevel, grid.cValues [7], grid.cValues [4], grid.val [7], grid.val [4]);
			}
			if ((et & 256) > 0) {
				grid.vertlist [8] =
					VertexInterp (isolevel, grid.p [0], grid.p [4], grid.val [0], grid.val [4]);
				grid.clist [8] =
					ValInterp (isolevel, grid.cValues [0], grid.cValues [4], grid.val [0], grid.val [4]);
			}
			if ((et & 512) > 0) {
				grid.vertlist [9] =
					VertexInterp (isolevel, grid.p [1], grid.p [5], grid.val [1], grid.val [5]);
				grid.clist [9] =
					ValInterp (isolevel, grid.cValues [1], grid.cValues [5], grid.val [1], grid.val [5]);
			}
			if ((et & 1024) > 0) {
				grid.vertlist [10] =
					VertexInterp (isolevel, grid.p [2], grid.p [6], grid.val [2], grid.val [6]);
				grid.clist [10] =
					ValInterp (isolevel, grid.cValues [2], grid.cValues [6], grid.val [2], grid.val [6]);
			}
			if ((et & 2048) > 0) {
				grid.vertlist [11] =
					VertexInterp (isolevel, grid.p [3], grid.p [7], grid.val [3], grid.val [7]);
				grid.clist [11] =
					ValInterp (isolevel, grid.cValues [3], grid.cValues [7], grid.val [3], grid.val [7]);
			}

			/* Create the triangle */
			ntriang = 0;
			for (i=0;triTable[cubeindex,i]!=-1;i+=3) {
				grid.triangles[ntriang].p[0] = grid.vertlist[triTable[cubeindex,i  ]];
				grid.triangles[ntriang].p[1] = grid.vertlist[triTable[cubeindex,i+1]];
				grid.triangles[ntriang].p[2] = grid.vertlist[triTable[cubeindex,i+2]];
				grid.triangles [ntriang].colors [0] = cm.eval (grid.clist [triTable [cubeindex, i]]);
				grid.triangles [ntriang].colors [1] = cm.eval (grid.clist [triTable [cubeindex, i+1]]);
				grid.triangles [ntriang].colors [2] = cm.eval (grid.clist [triTable [cubeindex, i+2]]);
				ntriang++;
			}

			return(ntriang);
		}

