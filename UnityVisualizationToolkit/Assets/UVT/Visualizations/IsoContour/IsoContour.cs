	public static float ValInterp(float isolevel, float cval1, float cval2,float valp1,float valp2) {
		float mu;
		float c;

		if (Mathf.Abs(isolevel-valp1) < tolerance)
			return(cval1);
		if (Mathf.Abs(isolevel-valp2) < tolerance)
			return(cval2);
		if (Mathf.Abs(valp1-valp2) < tolerance)
			return(cval1);
		mu = (isolevel - valp1) / (valp2 - valp1);
		c = cval1 + mu * (cval2 - cval1);

		return(c);
	}


	public static Vector3 VertexInterp(float isolevel,Vector3 p1,Vector3 p2,float valp1,float valp2) {
		float mu;
		Vector3 p = new Vector3();

		if (Mathf.Abs(isolevel-valp1) < tolerance)
			return(p1);
		if (Mathf.Abs(isolevel-valp2) < tolerance)
			return(p2);
		if (Mathf.Abs(valp1-valp2) < tolerance)
			return(p1);
		mu = (isolevel - valp1) / (valp2 - valp1);
		p = p1 + mu * (p2 - p1);

		return(p);
	}

