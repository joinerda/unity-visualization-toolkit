	public static void buildCubes(float [] x, float [] y, float [] z, float [,,] values , float isolevel) {
		int nx = x.Length;
		int ny = y.Length;
		int nz = z.Length;
		GridCell grid = new GridCell ();
		TriangleList tl = new TriangleList ();
		for (int i = 0; i < nx-1; i++) {
			for (int j = 0; j < ny-1; j++) {
				for (int k = 0; k < nz-1; k++) {
					float min_val = Mathf.Min (values [i, j, k],
						                Mathf.Min (values [i + 1, j, k],
							                Mathf.Min (values [i, j + 1, k],
								                Mathf.Min (values [i + 1, j + 1, k],
									                Mathf.Min (values [i, j, k + 1],
										                Mathf.Min (values [i + 1,j,k + 1],
											                Mathf.Min (values [i, j + 1, k + 1],
												                values [i + 1, j + 1, k + 1])))))));
					float max_val = Mathf.Max (values [i, j, k],
						Mathf.Max (values [i + 1, j, k],
							Mathf.Max (values [i, j + 1, k],
								Mathf.Max (values [i + 1, j + 1, k],
									Mathf.Max (values [i, j, k + 1],
										Mathf.Max (values [i + 1,j,k + 1],
											Mathf.Max (values [i, j + 1, k + 1],
												values [i + 1, j + 1, k + 1])))))));
					if (isolevel > min_val && isolevel < max_val) {
						GameObject go = GameObject.CreatePrimitive (PrimitiveType.Cube);
						go.transform.position = new Vector3 (0.5f * (x [i] + x [i + 1]), 
							0.5f * (y [j] + y [j + 1]), 
							0.5f * (z [k] + z [k + 1]));
						go.transform.localScale = new Vector3 ((x [i + 1] - x [i]), y [j + 1] - y [j], z [k + 1] - z [k]);
					}
				}
			}
		}
	}

